public abstract class FiguraUmaUnidade {
    //Atributos
    private Integer unidade;
    

    //Construtores
    public FiguraUmaUnidade() {

    }

    public FiguraUmaUnidade(Integer unidade) {
        this.unidade = unidade;
    }

    //Getter e Setter
    public Integer getUnidade() {
        return unidade;
    }

    public void setUnidade(Integer unidade) {
        this.unidade = unidade;
    }    
}
