package entities;

public class Departament {
    //Atributo
    private String name;

    //Construtor
    public Departament() {
    }

    public Departament(String name) {
        this.name = name;
    }

    //Getters e Setters
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
